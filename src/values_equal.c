#include <stdio.h>
#include <stdlib.h>
#include <assert.h>
#include <string.h>

#include "octo.h"
#include "octo_types.h"

int values_equal(SqlValue *a, SqlValue *b) {
	if (a->type != b->type)
		return FALSE;
	if (strcmp(a->v.string_literal, b->v.string_literal) == 0)
		return TRUE;
	return FALSE;
}
